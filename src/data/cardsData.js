        // 2 Punkte: Der Mitarbeiter hat Grundfunktionen ausprobiert und einfache Tutorials durchgearbeitet, braucht aber noch Anleitung.

      // Effizienz und Selbstständigkeit (3-4 Punkte)
        // 3 Punkte: Der Mitarbeiter kann selbstständig Standardaufgaben in PowerMill bearbeiten, sucht aber gelegentlich nach der optimalen Lösung oder benötigt Hilfe bei komplexeren Herausforderungen.
