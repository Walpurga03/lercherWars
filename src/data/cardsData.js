      // Expertise und Innovation (5-6 Punkte)
        // 5 Punkte: Der Mitarbeiter ist sehr versiert in PowerMill, entwickelt eigene Lösungen für komplexe Probleme und beginnt, Workflows zu optimieren.
        // 6 Punkte: Der Mitarbeiter gilt als PowerMill-Experte, kennt alle Funktionen und Tricks, teilt sein Wissen mit Kollegen und kann innovative Prozesse zur Effizienzsteigerung entwickeln.


  //2.Sauberkeit
      // Grundlegende Ordnung (1-2 Punkte)  
        // 1 Punkt: Der Arbeitsplatz ist oft unordentlich, Werkzeuge und Materialien sind nicht am vorgesehenen Platz. Aufräumen geschieht selten und meist nur auf Aufforderung.
        // 2 Punkte: Der Mitarbeiter bemüht sich um Ordnung, lässt aber häufig Gegenstände herumliegen. Es gibt Verbesserungspotenzial, aber die Grundlagen sind erkennbar.

      // Regelmäßige Pflege (3-4 Punkte)
        // 3 Punkte: Der Arbeitsplatz ist in der Regel ordentlich. Werkzeuge und Materialien werden meist nach Gebrauch weggeräumt, gelegentlich gibt es aber noch Unordnung.
        // 4 Punkte: Der Mitarbeiter hält seinen Arbeitsplatz sauber und ordentlich. Werkzeuge und Materialien sind immer richtig sortiert und aufgeräumt, mit seltenen Ausnahmen.

      // Vorbildliche Sauberkeit (5-6 Punkte)
        // 5 Punkte: Der Arbeitsplatz ist vorbildlich gepflegt. Der Mitarbeiter geht proaktiv vor, um Sauberkeit und Ordnung zu gewährleisten und inspiriert andere, es ihm gleichzutun.
        // 6 Punkte: Der Mitarbeiter setzt den Standard für Sauberkeit und Ordnung. Sein Arbeitsplatz könnte als Vorzeigebeispiel dienen, und er nimmt aktiv an Initiativen zur Verbesserung der Arbeitsumgebung teil.


  //3.Fräserfahrung
      // Einstiegsniveau (1-2 Punkte)
        // 1 Punkt: Der Mitarbeiter hat grundlegende Kenntnisse der Maschinenbedienung, benötigt aber umfassende Anleitung und hat wenig bis keine eigenständige Bearbeitungserfahrung.
        // 2 Punkte: Der Mitarbeiter kann einfache Aufgaben selbstständig durchführen, hat aber begrenzte Erfahrung mit komplexeren Projekten und benötigt gelegentlich Unterstützung.

      // Mittleres Erfahrungsniveau (3-4 Punkte)
        // 3 Punkte: Der Mitarbeiter hat eine solide Erfahrungsbasis, kann die meisten Aufgaben ohne Hilfe ausführen und löst Routineprobleme eigenständig.
        // 4 Punkte: Der Mitarbeiter zeigt hohe Kompetenz in der Bedienung der Maschinen, bearbeitet auch anspruchsvollere Projekte effizient und findet kreative Lösungen für Herausforderungen.

      // Hohe Expertise (5-6 Punkte)
        // 5 Punkte: Der Mitarbeiter verfügt über umfangreiche Erfahrung, meistert komplexe Bearbeitungsaufgaben souverän und teilt sein Wissen aktiv mit weniger erfahrenen Kollegen.
        // 6 Punkte: Der Mitarbeiter gilt als Experte, beherrscht auch spezielle Frästechniken, arbeitet an den anspruchsvollsten Projekten und ist in der Lage, Prozesse und Techniken weiterzuentwickeln.


  //4.Sportlichkeit
      // Couch-Potato (1 Punkt)
        // Nimmt selten bis nie an körperlicher Aktivität teil, zieht entspannte Freizeitaktivitäten vor.

      // Gelegenheitssportler (2 Punkte)
        // Betreibt gelegentlich Sport oder körperliche Aktivitäten, wie gelegentliches Spazierengehen oder Radfahren.

      // Regelmäßiger Aktiver (3 Punkte)
        // Engagiert sich regelmäßig in einer Form von mäßiger körperlicher Aktivität, wie regelmäßiges Reiten, Kegeln oder Fitnessstudio-Besuche.

      // Teamspieler / Individualsportler (4 Punkte)
        // Betreibt regelmäßig eine spezifische Individualsportart, zeigt dabei Einsatz und Motivation.

      // Leidenschaftlicher Sportler (5 Punkte)
        // Betreibt Sport mit hohem Engagement, nimmt an Wettkämpfen teil oder hat eine besondere Leidenschaft für eine oder mehrere Sportarten, die einen signifikanten Teil seiner Freizeit einnehmen.

      // Spitzensportler (6 Punkte)
        // Erreicht in seiner bevorzugten Sportart ein hohes Leistungsniveau, möglicherweise mit Auszeichnungen oder auf Wettkampfniveau, und dient als Inspiration für andere.



